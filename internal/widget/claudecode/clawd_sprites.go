// GetToolIcon returns the appropriate icon for a tool name
func GetToolIcon(toolName string) *ClawdSprite {
	switch toolName {
	case "Bash":
		return &ToolIconBash
	case "Read":
		return &ToolIconRead
	case "Edit", "Write":
		return &ToolIconEdit
	case "Glob", "Grep":
		return &ToolIconSearch
	case "WebFetch", "WebSearch":
		return &ToolIconWeb
	case "Task":
		return &ToolIconTask
	default:
		return nil
	}
}
